LEVEL_ORDER = ["wide", "turn", "zero", "maze_c", "split_maze", "empty"]

DIRS = {
    "^": (0, -1),
    ">": (1, 0),
    "v": (0, 1),
    "<": (-1, 0),
}
DIR_TO_CHAR = {v: k for k, v in DIRS.items()}
SINK = "S"


@dataclass(frozen=True)
class Emitter:
    id: int
    x: int
    y: int
    dx: int
    dy: int


def parse_level(lines):
    h = len(lines)
    w = max(len(r) for r in lines)
    walls = set()
    emitters = []
    sinks = set()
    for y, row in enumerate(lines):
        for x, ch in enumerate(row):
            if ch == "#":
                walls.add((x, y))
            elif ch in DIRS:
                dx, dy = DIRS[ch]
                emitters.append(Emitter(len(emitters), x, y, dx, dy))
            elif ch == SINK:
                sinks.add((x, y))
    return w, h, walls, emitters, sinks


def get_level(name):
    if name in LEVELS:
        return LEVELS[name]
    path = os.path.join(CUSTOM_LEVELS_DIR, f"{name}.json")
    if os.path.isfile(path):
        with open(path) as f:
            data = json.load(f)
        return data["lines"]
    raise ValueError(f"Unknown level '{name}'. Known levels: {', '.join(sorted(LEVELS))}")


def _level_to_lines(w, h, walls, emitters, sinks):
    grid = [['.' for _ in range(w)] for _ in range(h)]
    for (x, y) in walls:
        if 0 <= y < h and 0 <= x < w:
            grid[y][x] = '#'
    for (x, y) in sinks:
        if 0 <= y < h and 0 <= x < w:
            grid[y][x] = 'S'
    for emitter in emitters:
        if 0 <= emitter.y < h and 0 <= emitter.x < w:
            grid[emitter.y][emitter.x] = DIR_TO_CHAR.get((emitter.dx, emitter.dy), '>')
    return [''.join(row) for row in grid]


def save_level_json(name, w, h, walls, emitters, sinks):
    os.makedirs(CUSTOM_LEVELS_DIR, exist_ok=True)
    lines = _level_to_lines(w, h, walls, emitters, sinks)
    data = {"name": name, "lines": lines}
    path = os.path.join(CUSTOM_LEVELS_DIR, f"{name}.json")
    with open(path, 'w') as f:
        json.dump(data, f, indent=2)
    return path


def get_custom_level_names():
    if not os.path.isdir(CUSTOM_LEVELS_DIR):
        return []
    return [
        fname[:-5]
        for fname in sorted(os.listdir(CUSTOM_LEVELS_DIR))
        if fname.endswith('.json')
    ]
